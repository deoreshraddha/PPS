#include<iostream>
using namespace std ;

int main()
{
    int a = 7, b = 4;
    
    //Addition operator 
    cout<<"a+b="<<(a+b)<<endl;
    
    //Subtraction operator 
    cout<<"a-b="<<(a-b)<<endl;
    
    //Multiplication operator 
    cout<<"a*b="<<(a*b)<<endl;
    
    //Division operator 
    cout<<"a/b="<<(a/b)<<endl;
    
    //Modulo operator 
    cout<<"a%b="<<(a%b)<<endl;
    
    return 0;
}
    
